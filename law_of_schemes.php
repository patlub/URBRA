<div class="row">
<div class="col-md-10">
<div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">


<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingSeven">
        <h4 class="panel-title">
            <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseSeven"
               aria-expanded="true" aria-controls="collapseSeven">
                Establishment of a retirement benefits scheme
            </a>
        </h4>
    </div>
    <div id="collapseSeven" class="panel-collapse collapse in" role="tabpanel"
         aria-labelledby="headingSeven">
        <div class="panel-body">
            <p>(1) A person shall not establish or operate a retirement benefits
                scheme except under a licence issued by the Authority in accordance
                with this Act.</p>

            <p>(2) For the avoidance of doubt, all existing retirement benefits
                schemes established by a written law shall be required to obtain a
                licence issued in accordance with this Act.</p>

        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingOne">
        <h4 class="panel-title">
            <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne"
               aria-expanded="true" aria-controls="collapseOne">
                Application and grant of licence of a retirement benefits scheme
            </a>
        </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel"
         aria-labelledby="headingOne">
        <div class="panel-body">
            <p>(1) An application for a licence to establish a retirement benefits
                scheme shall be in the prescribed form and shall be accompanied by
                the prescribed fee.</p>

            <p>(2) An application for a licence to establish a retirement benefits
                scheme shall state—</p>
            <ol class="point">
                <li>the name and address of the applicant;
                </li>
                <li>the name of the proposed retirement benefits scheme;
                </li>
                <li>names and addresses of the custodian, trustee, administrator
                    and fund manager of the retirement benefits scheme;
                </li>
                <li>the status of the retirement benefits scheme in respect of—
                    <ul style="list-style-type: lower-roman;">
                        <li>the names of members admitted into the retirement benefits scheme and their contribution;
                        </li>
                        <li>benefits that accrue to members under the retirement
                            benefits scheme;
                        </li>
                        <li>whether any members of the scheme are active
                            members or not;
                        </li>
                    </ul>
                </li>
                <li>the proposed scheme rules which shall adequately protect
                    the interests of members and beneficiaries of the scheme;
                </li>
                <li>the address of a place in Uganda for the service on the
                    applicant of any notice or document required or authorised
                    to be served on the applicant under this Act; and
                </li>
                <li>any other information which the Authority considers
                    necessary for the purposes of determining the application.
                </li>
            </ol>
            <p>(3) A person who establishes or operates a retirement benefits
                scheme without a licence commits an offence and is liable on
                conviction to a fine not exceeding one thousand five hundred
                currency points or imprisonment not exceeding twelve years, or both.</p>

        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingTwo">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                Grant of licence to operate a retirement benefits scheme
            </a>
        </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
        <div class="panel-body">
            <p>(1) The Authority may grant a licence to operate a retirement
                benefits scheme, if the Authority is satisfied that—</p>
            <ol class="point">
                <li>the proposed retirement benefits scheme is established as
                    an irrevocable trust under this Act and Regulations made
                    under this Act;
                </li>
                <li>the scheme rules of the proposed retirement benefits
                    scheme adequately protect the rights and interests of the
                    members and beneficiaries of the scheme; and
                </li>
                <li>the custodian, trustee, administrator and fund manager of
                    the proposed retirement benefits scheme are licensed in
                    accordance with this Act.
                </li>
            </ol>
            <p>(2) The Authority shall publish in the Gazette and in a
                newspaper of wide circulation in Uganda, a list of all retirement benefits schemes licensed under this
                Act at least once in every year.</p>

            <p>(3) The Authority may refuse to grant a licence to an applicant,
                if the Authority is satisfied that—</p>
            <ol class="point">
                <li>the information contained in the application is false in any
                    material particular;
                </li>
                <li>the applicant does not meet the requirements specified in
                    section 29 (2) and subsection 30 (1); or
                </li>
                <li>the applicant is not a fit and proper person as prescribed in
                    Schedule 3.
                </li>
            </ol>
            <p>(4) Where the Authority refuses to grant a licence to an
                applicant, the Authority shall notify the applicant of its decision and
                specify the reasons for the refusal in writing.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingThree">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                Validity of licence
            </a>
        </h4>
    </div>
    <div id="collapseThree" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingThree">
        <div class="panel-body">
            <p>A licence issued to a retirement benefits scheme shall be valid from
                the date of issue and shall remain in force until the scheme is wound
                up in accordance with the scheme rules or the law under which the
                scheme is established or until the licence is revoked under this Act.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFour">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFour" aria-expanded="false" aria-controls="collapseFour">
                Revocation of licence
            </a>
        </h4>
    </div>
    <div id="collapseFour" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFour">
        <div class="panel-body">
            <p>(1) A licence issued to an administrator shall be valid for one
                year and may be renewed upon payment of the prescribed fee.</p>

            <p>(2) An application for the renewal of a licence under subsection (1)
                shall be made at least three months before the expiry of the licence.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFive">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFive" aria-expanded="false" aria-controls="collapseFive">
                Revocation of licence of Administrator
            </a>
        </h4>
    </div>
    <div id="collapseFive" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFive">
        <div class="panel-body">
            <p>(1) The Authority may revoke the licence of a retirement
                benefits scheme if—</p>
            <ol class="point">
                <li>the Authority discovers that the applicant made a false
                    statement in the application, which he or she knew to be
                    false in any material particular;
                </li>
                <li>the retirement benefits scheme is merged, wound up or
                    dissolved;
                </li>
                <li>the retirement benefits scheme breaches the conditions of
                    its licence;
                </li>
                <li>the licence expires; or
                </li>
                <li>the retirement benefits scheme contravenes any provision of this Act or any regulations made under
                    this Act.
                </li>
            </ol>
            <p>(2) The Authority shall give the trustee of a retirement benefits
                scheme at least thirty days notice in writing specifying the reasons for
                the intended revocation of the licence.</p>

            <p>(3) The Authority shall, before revoking a licence of a retirement
                benefits scheme, consider any representations made in writing by the
                sponsor opposing the revocation.</p>

            <p>(4) The Authority shall revoke the licence of a retirement
                benefits scheme if—</p>
            <ol>
                <li>within thirty days after issuing the notice, the trustee of the
                    retirement benefits scheme has not opposed the revocation, or
                </li>
                <li>the Authority is not satisfied by the representation made by
                    the trustee under subsection (3).
                </li>
            </ol>
            <p>The revocation of a licence of a retirement benefits scheme
                shall not in any way prejudice the rights of members and beneficiaries
                under the retirement benefits scheme.</p>

            <p>(6) Where the licence of a retirement benefits scheme is revoked,
                the Authority may, subject to the approval of the Board in
                consultation with the Minister, take over the distribution of the assets
                and the supervision of the retirement benefits scheme or appoint an
                interim administrator in order to protect the interests of members and
                beneficiaries of the retirement benefits scheme.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingSix">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseSix" aria-expanded="false" aria-controls="collapseSix">
                Functions of a Administrator
            </a>
        </h4>
    </div>
    <div id="collapseSix" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingSix">
        <div class="panel-body">
            <p>(1) An administrator of a retirement benefits scheme shall—</p>
            <ol class="point">
                <li>keep the records of the scheme;
                </li>
                <li>process receipts and invoices;
                </li>
                <li>produce quarterly and annual accounts for audit;
                </li>
                <li>organise and arrange for meetings ; and</li>
                <li>prepare annual benefit statements and trustee reports.
                </li>
            </ol>
            <p>(2) An administrator of a retirement benefits scheme shall not
                act as a custodian, trustee or fund manager of the same retirement
                benefits scheme.</p>

            <p>(3) The provisions of subsection (2) shall apply to any assignee or related party of the
                administrator.</p>
        </div>
    </div>
