                Application and grant of licence of trustee
            </a>
        </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel"
         aria-labelledby="headingOne">
        <div class="panel-body">
            <p>(1) An application for a licence to act as a trustee of a retirement
                benefits scheme shall be in the prescribed form and shall</p>
            <ol class="point">
                <li>state whether the applicant has the adequate professional,
                    technical knowledge, experience or operational ability to
                    perform the functions of a trustee;
                </li>
                <li>contain or be accompanied by any other information that
                    the Authority may require for the purpose of determining
                    the application;
                </li>
                <li>contain the address of a place in Uganda for the service on
                    the applicant of any notice or document required or
                    authorised to be served on the applicant under this Act; and be accompanied by the
                    prescribed fee.
                </li>
            </ol>
            <p>(2) The Authority may grant a licence to an applicant who meets the requirements
                specified in
                subsection (1).</p>

            <p>(3) The Authority shall publish in the Gazette and in a
                newspaper with wide circulation, a list of all licensed trustees, at least
                once in every year.</p>

            <p>(4) A person who acts as a trustee of a retirement benefits
                scheme without a licence issued under this Act commits an offence
                and is liable on conviction to a fine not exceeding one thousand
                currency points or imprisonment not exceeding six years or both.</p>


        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingTwo">
        <h4 class="panel-title">
            <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo"
               aria-expanded="true" aria-controls="collapseTwo">
                Refusal of licence of trustee
            </a>
        </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="headingTwo">
        <div class="panel-body">
            <p>(1) The Authority may refuse to grant a licence to an applicant
                if the Authority is satisfied that</p>
            <ol class="point">
                <li>the information contained in the application is false in any
                    material particular;
                </li>
                <li>the applicant is not a fit and proper person as prescribed in
                    Part II of Schedule 3;
                </li>
                <li>the applicant has ever been a trustee of a retirement benefits
                    scheme whose licence was revoked by the Authority due to
                    any fault either fully or partially of the applicant; or
                </li>
                <li>the applicant does not meet the requirements specified in
                    section 41(1).
                </li>
            </ol>
            <p>(2) Where the Authority refuses to grant a licence to an
                applicant, the Authority shall notify the applicant of its decision and
                specify the reasons for the refusal in writing.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingThree">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                Restrictions on licence of trustee
            </a>
        </h4>
    </div>
    <div id="collapseThree" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingThree">
        <div class="panel-body">
            <p>(1) A person shall not be licensed as a trustee of a retirement benefits
                scheme if he or she</p>
            <ol class="point">
                <li>has been sentenced to imprisonment by a court of
                    competent jurisdiction for six months or more;
                </li>
                <li>is declared bankrupt or insolvent, or has entered into an arrangement with his or
                    her creditors;
                </li>
                <li>was previously involved in the management or
                    administration of a retirement benefits scheme whose
                    licence has been revoked under this Act, due to his or her
                    fault either fully or partially;
                </li>
                <li>is disqualified under any other written law, or his or her
                    holding office as such is determined by the Authority as
                    being, in any way, detrimental to the retirement benefits
                    scheme; or
                </li>
                <li>is not a fit and proper person as prescribed in Schedule 3.</li>
            </ol>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFour">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFour" aria-expanded="false" aria-controls="collapseFour">
                Validity of licence of trustee
            </a>
        </h4>
    </div>
    <div id="collapseFour" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFour">
        <div class="panel-body">
            <p>(1) A licence issued to a trustee shall be valid for one year and
                may be renewed upon payment of the prescribed fee.</p>

            <p>(2) An application for the renewal of a licence under subsection (1)
                shall be made at least three months before the expiry of the licence.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFive">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFive" aria-expanded="false" aria-controls="collapseFive">
                Revocation of licence of trustee
            </a>
        </h4>
    </div>
    <div id="collapseFive" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFive">
        <div class="panel-body">
            <p>(1) The Authority may revoke the licence of a trustee if</p>
            <ol class="point">
                <li>the Authority discovers, after the licence has been issued,
                    that the trustee made a false statement in relation to the
                    application;
                </li>
                <li>an event occurs which renders the trustee ineligible to
                    perform his or her functions as a trustee;
                </li>
                <li>the trustee is declared bankrupt or insolvent, or enters into
                    an arrangement with his or her creditors;
                </li>
                <li>the trustee is declared bankrupt or insolvent, or enters into
                    an arrangement with his or her creditors;
                </li>
                <li>the trustee is in breach of any condition of the licence;</li>
                <li>the trustee is disqualified by virtue of section 43;</li>
                <li>the trustee ceases to be a fit and proper person as prescribed
                    in Part II of Schedule 3; or
                </li>
                <li>the trustee does not comply with any provision of this Act, or any regulations made under this
                    Act.
                </li>
            </ol>
            <p>(2) The Authority shall give the trustee at least thirty days notice
                in writing specifying the reasons for the intended revocation of the
                licence.</p>

            <p>(3) The Authority shall, before revoking a licence of a trustee,
                consider any representations made in writing by the trustee opposing
                the revocation.</p>

            <p>(4) The Authority shall revoke the licence of a trustee ifâ€”</p>
            <ol>
                <li>within thirty days after issuing the notice, the trustee has
                    not opposed the revocation; or
                </li>
                <li>the Authority is not satisfied by the representation made by
                    the trustee under subsection (3).
                </li>
            </ol>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingSix">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseSix" aria-expanded="false" aria-controls="collapseSix">
                Functions of a trustee
            </a>
        </h4>
    </div>
    <div id="collapseSix" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingSix">
        <div class="panel-body">
            <p>(1) A trustee of a retirement benefits scheme shall</p>
            <ol class="point">
                <li>manage and oversee the operation of the retirement benefit
                    scheme in accordance with this Act and regulations made
                    under this Act;
                </li>
                <li>provide for the proper investment of scheme funds in
                    accordance with this Act and Regulations made under this Act;
                </li>
                <li>make arrangements for the payment of benefits in
                    accordance with the scheme rules;
                </li>
                <li>keep financial and membership records of the scheme;</li>
                <li>open and maintain bank accounts necessary for the exercise
                    of the functions of the scheme;
                </li>
                <li>appoint a custodian, fund manager, administrator or an
                    actuary; and
                </li>
                <li>appoint an auditor to audit the accounts of the scheme.</li>
            </ol>
            <p>(2) A trustee shall perform his or her functions in accordance
                with sound financial principles and shall ensure, as far as possible,
                that the revenue of the retirement benefits scheme is sufficient to meet
                expenditure properly charged to revenue.</p>

            <p>(3) A trustee shall not act as an administrator, custodian, actuary
                or fund manager of the same retirement benefits scheme.</p>

            <p>(4) The provisions of subsection (3) shall apply to any assignee
                or related party of the trustee.</p>
