                Licensing of Custodian
            </a>
        </h4>
    </div>
    <div id="collapseSeven" class="panel-collapse collapse in" role="tabpanel"
         aria-labelledby="headingOne">
        <div class="panel-body">
            <p>An applicantion for a licence as a custodian of a retirement benefits
                scheme shall not be granted—</p>
            <ol class="point">
                <li>unless the applicant is a financial institution licensed under
                    the Financial Institutions Act, 2004;
                </li>
                <li>if the applicant does not have the adequate professional,
                    technical and operational systems to perform the functions
                    of a custodian of a retirement benefits scheme;
                </li>
                <li>if the applicant has been a custodian of a retirement benefits
                    scheme whose licence was revoked by the Authority due to
                    any fault either fully or partially of the custodian; and
                </li>
                <li>if the applicant does not meet any additional requirements
                    as may be prescribed by this Act or regulations made under
                    this Act.</li>
            </ol>

        </div>
    </div>
</div>

<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingOne">
        <h4 class="panel-title">
            <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne"
               aria-expanded="true" aria-controls="collapseOne">
                Application and grant of licence of custodian
            </a>
        </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel"
         aria-labelledby="headingOne">
        <div class="panel-body">
            <p>(1) An application for a licence to act as a custodian shall be in
                the prescribed form and shall—</p>
            <ol class="point">
                <li>contain or be accompanied by any other information that
                    the Authority may require for the purpose of determining
                    the application;
                </li>
                <li>contain the address of a place in Uganda for the service on
                    the applicant of any notice or document required or
                    authorised to be served on the applicant under this Act; and
                </li>
                <li>be accompanied by the prescribed fee.
                </li>
            </ol>
            <p>(2) The Authority shall grant a licence to an applicant which
                meets the requirements specified in section 34 and subsection 35(1).</p>

            <p>(3) The Authority shall publish in the Gazette and a newspaper
                with wide circulation, a list of all licensed custodians at least once every year.</p>

            <p>(4) A financial institution which acts as a custodian of a
                retirement benefits scheme without a licence issued under this Act
                commits an offence and is liable on conviction to a fine not exceeding
                one thousand currency points.</p>


        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingTwo">
        <h4 class="panel-title">
            <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo"
               aria-expanded="true" aria-controls="collapseTwo">
                Refusal of licence of custodian
            </a>
        </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
        <div class="panel-body">
            <p>(1) The Authority may refuse to grant a licence to an applicant,
                if the Authority is satisfied that—</p>
            <ol class="point">
                <li>the information contained in the application is false in any
                    material particular; or
                </li>
                <li>the applicant does not meet the requirements specified in
                    section 34 and subsection 35 (1).
                </li>
            </ol>
            <p>(2) Where the Authority refuses to grant a licence to an
                applicant, the Authority shall notify the applicant of its decision and
                specify the reasons for the refusal in writing.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFour">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFour" aria-expanded="false" aria-controls="collapseFour">
                Validity of licence of custodian
            </a>
        </h4>
    </div>
    <div id="collapseFour" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFour">
        <div class="panel-body">
            <p>(1) A licence issued to a custodian shall be valid for one year and
                may be renewed upon payment of the prescribed fee.</p>

            <p>(2) An application for the renewal of a licence under subsection
                (1) shall be made at least three months before the expiry of the
                licence.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFive">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFive" aria-expanded="false" aria-controls="collapseFive">
                Revocation of licence of custodian
            </a>
        </h4>
    </div>
    <div id="collapseFive" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFive">
        <div class="panel-body">
            <p>(1) The Authority may revoke the licence of a custodian if—</p>
            <ol class="point">
                <li>the Authority discovers after the licence has been issued,
                    that the custodian made a false statement in relation to the
                    application;
                </li>
                <li>any event occurs which renders the custodian ineligible to
                    provide custodial services to the retirement benefits
                    scheme;
                </li>
                <li>the custodian's business is wound up;
                </li>
                <li>the custodian is in breach of any condition of the licence;
                </li>
                <li>the custodian ceases to be a licensed financial institution
                    under the Financial Institutions Act,2004; or</li>
                <li>the custodian does not comply with any provision of this
                    Act or regulations made under this Act.</li>
            </ol>
            <p>(2) The Authority shall give the custodian at least thirty days
                notice in writing specifying the reasons for the intended revocation of
                the licence.</p>

            <p>(3) The Authority shall, before revoking a licence of a custodian,
                consider any representations made in writing by the custodian
                opposing the revocation.</p>

            <p>(4) The Authority shall revoke the licence of a custodian if—</p>
            <ol>
                <li>within thirty days after issuing the notice, the custodian has
                    not opposed the revocation, or
                </li>
                <li>the Authority is not satisfied by the representation made by
                    the custodian under subsection (3).
                </li>
            </ol>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingSix">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseSix" aria-expanded="false" aria-controls="collapseSix">
                Functions of a custodian
            </a>
        </h4>
    </div>
    <div id="collapseSix" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingSix">
        <div class="panel-body">
            <p>(1) A custodian of a retirement benefits scheme shall—</p>
            <ol class="point">
                <li>hold the funds, assets and investments of the retirement
                    benefits scheme in safe custody for the members and
                    beneficiaries of the scheme;
                </li>
                <li>receive contributions made to the scheme;
                </li>
                <li>notify the administrator and fund manager of the receipt of
                    contributions;
                </li>
                <li>be responsible for collecting of dividends and income in
                    relation to the investments of the scheme;</li>
                <li>be responsible for processing and transferring documents;
                </li>
                <li>report to the Authority on matters relating to the assets and
                    investments of the scheme.
                </li>
            </ol>
            <p>(2) A custodian shall not use the funds or assets in its custody to
                meet its own financial obligations.</p>

            <p>(3) A custodian of a retirement benefits scheme shall not act as a
                trustee, administrator or fund manager of the same retirement benefits
                scheme.</p>

            <p>(4) The provisions of subsection (3) shall apply to any sub
                custodian, assignee or related party of the custodian.</p>
        </div>
    </div>
