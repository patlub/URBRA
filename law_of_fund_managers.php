<div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingOne">
        <h4 class="panel-title">
            <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne"
               aria-expanded="true" aria-controls="collapseOne">
                Application and grant of licence of Fund Managers
            </a>
        </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse in" role="tabpanel"
         aria-labelledby="headingOne">
        <div class="panel-body">
            <p>(1) An application for a licence as a fund manager of a retirement
                benefits scheme shall be in the prescribed form and shall—</p>
            <ol class="point">
                <li>state whether the applicant has the adequate professional,
                    technical and operational ability to perform the functions of
                    a fund manager;
                </li>
                <li>contain or be accompanied by any other information that
                    the Authority may require for the purpose of determining
                    the application;
                </li>
                <li>contain the address of a place in Uganda for the service on
                    the applicant of any notice or document required or
                    authorised to be served on the applicant under this Act; and
                </li>
                <li>be accompanied by the prescribed fee.</li>
            </ol>
            <p>(2) The Authority may grant a licence to an applicant who meets
                the requirements specified in subsection (1).</p>

            <p>(3) The Authority shall publish in the Gazette and in a
                newspaper with wide circulation, a list of all licensed fund managers,
                at least once in every year.</p>

            <p>(4) A person who acts as a fund manager of a retirement benefits
                scheme without a licence issued under this Act commits an offence
                and is liable on conviction to a fine not exceeding one thousand currency points or imprisonment not
                exceeding six years, or both.</p>

        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingTwo">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                Refusal of licence of Fund Managers
            </a>
        </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
        <div class="panel-body">
            <p>(1) The Authority may refuse to grant a licence to an applicant
                if the Authority is satisfied that—</p>
            <ol class="point">
                <li>the information contained in the application is false in any
                    material particular;
                </li>
                <li>the applicant is not a fit and proper person as prescribed in
                    Part II of Schedule 3;
                </li>
                <li>the applicant has ever been a fund manager of a retirement
                    benefits scheme whose licence was revoked by the Authority
                    due to any fault either fully or partially of the applicant; or
                </li>
                <li>the applicant does not meet the requirements specified in
                    section 55 (1).
                </li>
            </ol>
            <p>(2) Where the Authority refuses to grant a licence to an
                applicant, the Authority shall notify the applicant of its decision and
                specify the reasons for the refusal in writing.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingThree">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                Restrictions on licence of Fund Managers
            </a>
        </h4>
    </div>
    <div id="collapseThree" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingThree">
        <div class="panel-body">
            <p>(1) A person shall not be licensed as a fund manager of a retirement benefits
                scheme if he or she</p>
            <ol class="point">
                <li>has been sentenced to imprisonment by a court of
                    competent jurisdiction for six months or more;
                </li>
                <li>is declared bankrupt or insolvent, or has entered into an
                    arrangement with his or her creditors;
                </li>
                <li>was previously involved in the management or
                    administration of a retirement benefits scheme whose
                    licence has been revoked under this Act due to his or her
                    fault either fully or partially;
                </li>
                <li>is disqualified under any other written law, or his or her
                    holding office as such is determined by the Authority as
                    being, in any way, detrimental to the retirement benefits scheme; or
                </li>
                <li>is not a fit and proper person as prescribed in Part II of
                    Schedule 3.
                </li>
            </ol>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFour">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFour" aria-expanded="false" aria-controls="collapseFour">
                Validity of licence of Fund Managers
            </a>
        </h4>
    </div>
    <div id="collapseFour" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFour">
        <div class="panel-body">
            <p>(1) A licence issued to a fund manager shall be valid for one year
                and shall be renewed annually upon payment of the prescribed fee.</p>

            <p>(2) An application for the renewal of a licence under subsection
                (1) shall be made at least three months before the expiry of the
                licence.</p>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingFive">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseFive" aria-expanded="false" aria-controls="collapseFive">
                Revocation of licence of Fund Managers
            </a>
        </h4>
    </div>
    <div id="collapseFive" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingFive">
        <div class="panel-body">
            <p>(1) The Authority may revoke the licence of a fund manager if—</p>
            <ol class="point">
                <li>the Authority discovers, after the licence has been issued,
                    that the fund manager made a false statement in relation to
                    the application;
                </li>
                <li>any event occurs which renders the fund manager ineligible
                    to perform his or her functions as a trustee;
                </li>
                <li>the fund manager is declared bankrupt or insolvent, or
                    enters into an arrangement with his or her creditors;
                </li>
                <li>the fund manager is in breach of any condition of the
                    licence;
                </li>
                <li>the fund manager is disqualified by virtue of section 57;</li>
                <li>the fund manager ceases to be a fit and proper person as
                    prescribed in Part II of Schedule 3; or
                </li>
                <li>the fund manager does not comply with any provision of
                    this Act, or any regulations made under this Act.
                </li>
            </ol>
            <p>(2) The Authority shall give the fund manager at least thirty days
                notice in writing specifying the reasons for the intended revocation of the licence.</p>

            <p>(3) The Authority shall, before revoking a licence of a fund
                manager, consider any representations made in writing by the fund
                manager opposing the revocation.</p>

            <p>(4) The Authority shall revoke the licence of a fund manager
                if—</p>
            <ol>
                <li>within thirty days after issuing the notice, the fund manager
                    has not opposed the revocation; or
                </li>
                <li>the Authority is not satisfied by the representation made by
                    the fund manager under subsection (3).
                </li>
            </ol>
        </div>
    </div>
</div>
<div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingSix">
        <h4 class="panel-title">
            <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion"
               href="#collapseSix" aria-expanded="false" aria-controls="collapseSix">
                Functions of a Fund Managers
            </a>
        </h4>
    </div>
    <div id="collapseSix" class="panel-collapse collapse" role="tabpanel"
         aria-labelledby="headingSix">
        <div class="panel-body">
            <p>(1) A fund manager of a retirement benefits scheme shall</p>
            <ol class="point">
                <li>be responsible for management of funds and other assets of
                    the retirement benefits scheme for purposes of investment
                    in accordance with the scheme's investment policy and the
                    scheme rules;
                </li>
                <li>provide advisory services on the investment of the scheme
                    funds and disseminating information concerning the assets
                    available for investment of scheme funds.
                </li>
            </ol>
            <p>(2) A fund manager of a retirement benefits scheme shall not act
                as a custodian, trustee or administrator of the same retirement benefits
                scheme.</p>

            <p>(3) The provisions of subsection (2) shall apply to any assignee
                or related party of the fund manager.</p>
        </div>
